#define S5P4418_UART_CKSRCSEL0(x)         S5P4418_DEFINE_FIELD(x, UART_CKSRCSEL0)
#define S5P4418_UART_CLKDIV0(x)           S5P4418_DEFINE_FIELD(x, UART_CLKDIV0)
